function parseRows(tsv) {
  return String(tsv || '')
    .split('\n')
    .map((line) => line.trim())
    .filter(Boolean)
    .map((line) => {
      const [lotNo, barcode] = line.split('\t');
      return {
        lotNo: String(lotNo || '').trim(),
        barcode: normalizeSeedBarcode(barcode),
      };
    })
    .filter((row) => row.lotNo && row.barcode);
}

